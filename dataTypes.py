# Integer
int_data_type = 25
print(int_data_type, f"Type of {type(int_data_type).__name__}")

# Float
float_data_type = 3.14159
print(float_data_type, f"Type of {type(float_data_type).__name__}")

# String
string_data_type = "Hello, World!"
print(string_data_type, f"Type of {type(string_data_type).__name__}")

# Boolean
boolean_data_type = True
print(boolean_data_type, f"Type of {type(boolean_data_type).__name__}")

# List
# A list in Python is a collection of items that can be of any data type (including strings, integers, floats, and other lists). 
list_data_type = ["Apple", 1, True]
print(list_data_type, f"Type of {type(list_data_type).__name__}")

# Tuple
# Tuples have several key characteristics that make them useful in various scenarios:
# Immutability : Tuples cannot be modified after creation.
# Heterogeneous elements : Tuples can contain elements of different data types.
# Ordered collection : Tuples maintain the order of their elements.
# Fixed-size : Tuples are fixed-size and cannot grow or shrink.
tuple_data_type = ("Red", "Green", "Blue")
print(tuple_data_type, f"Type of {type(tuple_data_type).__name__}")

# Dictionary
# Dictionaries are unordered collections of key-value pairs used for data storage and retrieval.
dictionary_data_type = {"name": "Jane", "age": 35}
print(dictionary_data_type, f"Type of {type(dictionary_data_type).__name__}")